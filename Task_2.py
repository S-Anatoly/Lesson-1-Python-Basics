import fractions


# №1. шестнадцатеричное строковое представление

num = int(input('Введите целое число -> '))

#проверка
print(f'проверка = {hex(num)}')

digit = "0123456789abcdef"
hex_str = ""

#перевод числа
while num > 0:
    hex_str = digit[num % 16] + hex_str
    num = num // 16

print(f'шестнадцатеричное {num} -->  {hex_str}')

