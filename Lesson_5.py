# 2. Напишите однострочный генератор словаря, который принимает на вход три списка
# одинаковой длины: имена str, ставка int, премия str с указанием процентов вида «10.25%».
# В результате получаем словарь с именем в качестве ключа и суммой премии в качестве значения.
# Сумма рассчитывается как ставка умноженная на процент премии

def dict_sum(nam, bid_nam, prem):
    print({n: b + b / 100 * float(p.strip('%')) for n, b, p in zip(nam, bid_nam, prem)})


name = ['Tolik', 'Peter', 'Oleg']
bid = [9100, 532, 33000]
premium = ['10.1%', '15.9%', '9.6%']

dict_sum(name, bid, premium)

