# 1. Напишите функцию, которая принимает на вход строку — абсолютный путь до файла.
# Функция возвращает кортеж из трёх элементов: путь, имя файла, расширение файла.

def func(path):
    path = path.replace('.', '/')  # Замена точки на слеш для разделения

    *path_n, name, extension = path.split('/')  # распаковываем список

    tuple_data = ('/'.join(path_n), name, extension)  # запись в кортеж переменных
    return tuple_data


path_user = input('введите путь файла -> ')
g = func(path_user)
print(g)

