# 3. Создайте функцию генератор чисел Фибоначчи

def fib(n):
    a, b = 0, 1
    for i in range(n):
        yield a
        a, b = b, a + b


num = int(input('Введите кол-во чисел Фибоначчи -> '))
print(list(fib(num)))
